    c=dsolve(a,'x')
    set(handles.sin, 'string', char(c));
else
     c=dsolve(a,'x')
    set(handles.sin, 'string', char(c));
     d=dsolve(a,b,'x')
    set(handles.con, 'string', char(d));
end
